function setLanguage(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });

  const pageKey = document.body.dataset.page;
  if (pageKey && translations[lang].pages && translations[lang].pages[pageKey]) {
    const pageData = translations[lang].pages[pageKey];
    const titleEl = document.getElementById('story-title');
    const textEl = document.getElementById('story-text');
    if (titleEl && pageData.storyTitle) titleEl.innerHTML = pageData.storyTitle;
    if (textEl && pageData.storyText) textEl.innerHTML = pageData.storyText;
  }

  const translateBtn = document.getElementById('translate-btn');
  if (translateBtn) {
    translateBtn.textContent = translations[lang].translateButton;
  }
}

function toggleLanguage() {
  currentLang = currentLang === 'en' ? 'es' : 'en';
  setLanguage(currentLang);
}

document.addEventListener('DOMContentLoaded', () => {
  const translateBtn = document.getElementById('translate-btn');
  if (translateBtn) {
    translateBtn.addEventListener('click', toggleLanguage);
  }
  
  // Scroll to top when clicking on active link (current page)
  const activeLinks = document.querySelectorAll('a.active');
  activeLinks.forEach(link => {
    link.addEventListener('click', (e) => {
      e.preventDefault();
      window.scrollTo(0, 0);
    });
  });
  
  setLanguage(currentLang);
});
